  const SUPPORTED_LANGUAGE_CODES = new Set(["en", "fr", "es", "pt", "de", "it", "nl", "sw", "tw"]);

  function normalizeLanguage(language) {
    const normalized = String(language || "").trim().toLowerCase();
    return SUPPORTED_LANGUAGE_CODES.has(normalized) ? normalized : "en";
  }

  function localeFor(language) {
    return LANGUAGE_LOCALES[normalizeLanguage(language)] || LANGUAGE_LOCALES.en;
  }

  function t(key, replacements = {}, language = "en") {
    const normalizedLanguage = normalizeLanguage(language);
    const template = TRANSLATIONS[normalizedLanguage]?.[key] ?? TRANSLATIONS.en[key] ?? key;

    return String(template).replace(/\{(\w+)\}/g, (_, token) => {
      return replacements[token] ?? `{${token}}`;
    });
  }

  function localizeAssistantSuggestion(suggestion, language) {
    const key = ASSISTANT_SUGGESTION_KEYS[suggestion];
    return key ? t(key, {}, language) : suggestion;
  }

  window.BudgetFlowI18n = {
    localeFor,
    localizeAssistantSuggestion,
    normalizeLanguage,
    optionLabels: LANGUAGE_OPTION_LABELS,
    t
  };
